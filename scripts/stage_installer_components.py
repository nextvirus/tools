#!/usr/bin/env python3
"""
After PyInstaller, split dist output into core / photo / meeting trees for modular installers.

Sizes are uncompressed on-disk totals (approximate installed footprint). Classification uses
path heuristics; shared stacks (numpy, scipy, pymupdf, …) stay in core.
"""

from __future__ import annotations

import argparse
import json
import os
import shutil
import sys
from collections import defaultdict
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent

# Paths (relative, posix, lower) matching add-on only — everything else → core.
_PHOTO_MARKERS: tuple[str, ...] = (
    "onnxruntime",
    "onnxruntime-",
    "/rembg/",
    "\\rembg\\",
    "pymatting",
    "/skimage/",
    "\\skimage\\",
    "/sklearn/",
    "\\sklearn\\",
    "/cv2/",
    "\\cv2\\",
    "/numba/",
    "\\numba\\",
    "/llvmlite/",
    "\\llvmlite\\",
    "/pooch/",
    "\\pooch\\",
    "/imageio/",
    "\\imageio\\",
    "u2net_human_seg",
    "u2net.onnx",
    "rembg_models",
    "pdfgui/photo/",
    "pdfgui\\photo\\",
)

_MEETING_MARKERS: tuple[str, ...] = (
    "/sounddevice/",
    "\\sounddevice\\",
    "/vosk/",
    "\\vosk\\",
    "vosk_models",
    "pocketsphinx",
    "/flac-",
    "\\flac-",
    "flac-linux",
)


def _norm_rel(p: Path, base: Path) -> str:
    return p.relative_to(base).as_posix().lower()


def classify_component(rel_posix_lower: str) -> str:
    for m in _MEETING_MARKERS:
        if m in rel_posix_lower:
            return "meeting"
    for m in _PHOTO_MARKERS:
        if m in rel_posix_lower:
            return "photo"
    return "core"


def _bundle_root(platform: str) -> Path:
    if platform == "darwin":
        p = ROOT / "dist" / "tools.app"
        if p.is_dir():
            return p
        raise FileNotFoundError(f"Missing {p} (run PyInstaller on macOS first).")
    # Windows / Linux onedir
    p = ROOT / "dist" / "tools"
    if p.is_dir():
        return p
    raise FileNotFoundError(f"Missing {p} (run PyInstaller first).")


def _iter_files(root: Path) -> list[Path]:
    return [p for p in root.rglob("*") if p.is_file()]


def _fmt_mb(n: int) -> str:
    if n <= 0:
        return "0"
    mb = n / (1024 * 1024)
    if mb < 10:
        return f"{mb:.1f}"
    return f"{mb:.0f}"


def _write_inno_inc(out_dir: Path, sizes: dict[str, int], inc_path: Path) -> None:
    out_dir.mkdir(parents=True, exist_ok=True)
    total = sum(sizes.values())
    lines = [
        "; Auto-generated by scripts/stage_installer_components.py — do not edit by hand.",
        f'#define SizeCoreMB "{_fmt_mb(sizes["core"])}"',
        f'#define SizePhotoMB "{_fmt_mb(sizes["photo"])}"',
        f'#define SizeMeetingMB "{_fmt_mb(sizes["meeting"])}"',
        f'#define SizeTotalMB "{_fmt_mb(total)}"',
        "",
    ]
    inc_path.write_text("\n".join(lines), encoding="utf-8")

    meta = {
        "core_bytes": sizes["core"],
        "photo_bytes": sizes["photo"],
        "meeting_bytes": sizes["meeting"],
        "total_bytes": total,
        "core_mb": float(sizes["core"]) / (1024 * 1024),
        "photo_mb": float(sizes["photo"]) / (1024 * 1024),
        "meeting_mb": float(sizes["meeting"]) / (1024 * 1024),
    }
    (out_dir / "component_sizes.json").write_text(json.dumps(meta, indent=2), encoding="utf-8")


def _write_mac_distribution_xml(out_base: Path, sizes: dict[str, int], version: str) -> None:
    """Minimal installer-gui-script for productbuild (optional sub-packages)."""
    xml = f"""<?xml version="1.0" encoding="utf-8"?>
<installer-gui-script minSpecVersion="2">
    <title>tools</title>
    <options customize="always" require-scripts="false"/>
    <choices-outline>
        <line choice="core"/>
        <line choice="photo"/>
        <line choice="meeting"/>
    </choices-outline>
    <choice id="core" visible="false" enabled="false" selected="true" title="Core"
        description="Required application files (about {_fmt_mb(sizes['core'])} MB unpacked).">
        <pkg-ref id="com.tools.pkg.core"/>
    </choice>
    <choice id="photo" title="Photo / 照片换底" start_selected="true"
        description="rembg + onnx (about {_fmt_mb(sizes['photo'])} MB unpacked).">
        <pkg-ref id="com.tools.pkg.photo"/>
    </choice>
    <choice id="meeting" title="Meeting / 会议纪要" start_selected="true"
        description="Microphone + Vosk local ASR (about {_fmt_mb(sizes['meeting'])} MB unpacked).">
        <pkg-ref id="com.tools.pkg.meeting"/>
    </choice>
    <pkg-ref id="com.tools.pkg.core" version="{version}" auth="Root">#core.pkg</pkg-ref>
    <pkg-ref id="com.tools.pkg.photo" version="{version}" auth="Root">#photo.pkg</pkg-ref>
    <pkg-ref id="com.tools.pkg.meeting" version="{version}" auth="Root">#meeting.pkg</pkg-ref>
</installer-gui-script>
"""
    (out_base / "distribution.xml").write_text(xml, encoding="utf-8")


def stage(platform: str) -> int:
    bundle = _bundle_root(platform)
    if platform == "darwin":
        out_base = ROOT / "dist" / "installer" / "mac"
    else:
        out_base = ROOT / "dist" / "installer" / "win"

    for name in ("core", "photo", "meeting"):
        d = out_base / name
        if d.is_dir():
            shutil.rmtree(d)
        d.mkdir(parents=True, exist_ok=True)

    assignments: dict[str, list[str]] = defaultdict(list)
    size_by: dict[str, int] = defaultdict(int)

    for src in _iter_files(bundle):
        rel = _norm_rel(src, bundle)
        comp = classify_component(rel)
        assignments[comp].append(rel)
        size_by[comp] += src.stat().st_size

        dest = out_base / comp / src.relative_to(bundle)
        dest.parent.mkdir(parents=True, exist_ok=True)
        shutil.copy2(src, dest)

    # sanity: every file assigned exactly once; union covers bundle
    all_rel_lower = {_norm_rel(p, bundle) for p in _iter_files(bundle)}
    union: set[str] = set()
    for c in ("core", "photo", "meeting"):
        for r in assignments[c]:
            if r in union:
                print(f"duplicate assign: {r}", file=sys.stderr)
                return 1
            union.add(r)
    if union != all_rel_lower:
        print(
            f"partition mismatch missing={len(all_rel_lower - union)} extra={len(union - all_rel_lower)}",
            file=sys.stderr,
        )
        return 1

    for c in ("core", "photo", "meeting"):
        comp_dir = out_base / c
        has_file = any(p.is_file() for p in comp_dir.rglob("*"))
        if c == "core" and not has_file:
            print("core staging has no files", file=sys.stderr)
            return 1
        if not has_file:
            (comp_dir / ".tools_component_placeholder").write_bytes(b"")

    out_base.mkdir(parents=True, exist_ok=True)
    _write_inno_inc(out_base, dict(size_by), out_base / "component_sizes.inc")

    if platform == "darwin":
        ver = os.environ.get("TOOLS_PKG_VERSION", "0.0.0").lstrip("v").split("+")[0] or "0.0.0"
        _write_mac_distribution_xml(out_base, dict(size_by), ver)

    print(f"Staged installer components under {out_base}")
    for c in ("core", "photo", "meeting"):
        print(f"  {c}: {len(assignments[c])} files, ~{_fmt_mb(size_by[c])} MB")
    return 0


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument(
        "--platform",
        default=sys.platform,
        help="darwin | win32 | linux (default: current)",
    )
    args = ap.parse_args()
    plat = args.platform
    if plat.startswith("linux"):
        plat = "linux"
    elif plat == "win32":
        plat = "win32"
    elif plat == "darwin":
        plat = "darwin"
    else:
        print(f"Unsupported platform: {args.platform}", file=sys.stderr)
        return 1
    try:
        return stage(plat)
    except FileNotFoundError as e:
        print(str(e), file=sys.stderr)
        return 1


if __name__ == "__main__":
    raise SystemExit(main())
